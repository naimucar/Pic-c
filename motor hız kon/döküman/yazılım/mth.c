// saymaya devam eder. toplam 1 saniye sonunda tmr1 (sayac) ýn deðeri diskte 24 delik olduðu  /////
// ve ayar yani option registerinin tmr0 için deðeri 1/16 ya ayarlandýðu için elde edilen     /////
