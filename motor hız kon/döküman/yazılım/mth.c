// ilgili komutlar ile analog bilgi alýnarak bir deðiþkene ve ardýndan pwm darbe geniþliðine  /////
// atanýr. bu sayede motorun hýz kontrolüde yapýlmýþ olunur.  her satýr için gerekli açýklama /////
// aþaðýda yapýlmýþtýr                                                                        /////
