// bu arada  timer0 gelen palsleri sayar                                            //
//////////////////////////////////////////////////////////////////////////////////////
   for(i=0;i<10;i++)
   {                                                   
   delay_ms(100);
      if(input(pin_d3)) {goto ana_p;}  // durdurma butonuna basýldý ise ana_p ye git
   }
/////////////////////////////////////////////////////////////////////////////////////
sayac=get_timer1(); 
